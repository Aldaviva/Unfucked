    private static readonly JsonNamingPolicy NAMING_POLICY = new KernighanRitchieCEnumToLowerCamelCaseNamingPolicy();

    [Fact]
    public void NoConverter() {
        JsonSerializerOptions jsonOpts = new(JsonSerializerDefaults.Web) { ReadCommentHandling = JsonCommentHandling.Skip };

        string serialized = JsonSerializer.Serialize(new MyRecord(MyEnum.YES_OR_MAYBE), jsonOpts);
        serialized.Should().Be("""{"isYes":3}""", "serializing");
        MyRecord deserialized = JsonSerializer.Deserialize<MyRecord>(serialized, jsonOpts)!;
        deserialized.IsYes.Should().Be(MyEnum.YES_OR_MAYBE, "deserializing");
    }

    [Fact]
    public void DefaultStringConverter() {
        JsonSerializerOptions jsonOpts = new(JsonSerializerDefaults.Web) { ReadCommentHandling = JsonCommentHandling.Skip, Converters = { new JsonStringEnumConverter() } };

        string serialized = JsonSerializer.Serialize(new MyRecord(MyEnum.YES_OR_MAYBE), jsonOpts);
        serialized.Should().Be("""{"isYes":"YES_OR_MAYBE"}""", "serializing");
        MyRecord deserialized = JsonSerializer.Deserialize<MyRecord>(serialized, jsonOpts)!;
        deserialized.IsYes.Should().Be(MyEnum.YES_OR_MAYBE, "deserializing");
    }

    [Fact]
    public void CamelCaseStringConverter() {
        JsonSerializerOptions jsonOpts = new(JsonSerializerDefaults.Web)
            { ReadCommentHandling = JsonCommentHandling.Skip, Converters = { new JsonStringEnumConverter(namingPolicy: JsonNamingPolicy.CamelCase) } };

        string serialized = JsonSerializer.Serialize(new MyRecord(MyEnum.YES_OR_MAYBE), jsonOpts);
        serialized.Should().Be("""{"isYes":"yeS_OR_MAYBE"}""", "broken serializing");
        MyRecord deserialized = JsonSerializer.Deserialize<MyRecord>(serialized, jsonOpts)!;
        deserialized.IsYes.Should().Be(MyEnum.YES_OR_MAYBE, "deserializing");
    }

    [Fact]
    public void KRCCamelCaseStringConverter() {
        JsonSerializerOptions jsonOpts = new(JsonSerializerDefaults.Web)
            { ReadCommentHandling = JsonCommentHandling.Skip, Converters = { new JsonStringEnumConverter(namingPolicy: NAMING_POLICY) } };

        string serialized = JsonSerializer.Serialize(new MyRecord(MyEnum.YES_OR_MAYBE), jsonOpts);
        serialized.Should().Be("""{"isYes":"yesOrMaybe"}""", "serializing");
        MyRecord deserialized = JsonSerializer.Deserialize<MyRecord>(serialized, jsonOpts)!;
        deserialized.IsYes.Should().Be(MyEnum.YES_OR_MAYBE, "deserializing");
    }

    private sealed record MyRecord(MyEnum IsYes);

    [SuppressMessage("ReSharper", "InconsistentNaming")] // this exact spelling is the point of this class and its test
    private enum MyEnum {

        NO,
        YES,
        MAYBE,
        YES_OR_MAYBE

    }

    [Theory]
    [InlineData("a", "a")]
    [InlineData("A", "a")]
    [InlineData("aa", "aa")]
    [InlineData("AA", "aa")]
    [InlineData("FooBar", "fooBar")]
    [InlineData("FooBarBaz", "fooBarBaz")]
    [InlineData("FooBar_baz", "fooBarBaz")]
    [InlineData("foo_bar", "fooBar")]
    [InlineData("FOO_BAR", "fooBar")]
    [InlineData("FOO_BAR_BAZ", "fooBarBaz")]
    [InlineData("_foo_bar_baz", "_fooBarBaz")]
    [InlineData("__8021X", "__8021X")]
    public void CamelCaseSerializing(string input, string expected) {
        string actual = NAMING_POLICY.ConvertName(input);
        actual.Should().Be(expected);
    }

    [Theory]
    [MemberData(nameof(CamelCaseSerializingRealWorldData))]
    public void CamelCaseSerializingRealWorld(string input, string expected) {
        CamelCaseSerializing(input, expected);
    }

